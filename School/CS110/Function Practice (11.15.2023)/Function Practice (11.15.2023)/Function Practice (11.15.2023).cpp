double sum(int n1, int n2) 
{
	double nRes;
	nRes = n1 + n2;
	return(nRes);
}
double Diff(int n1, int n2)
{
	double nRes;
	nRes = n1 - n2;
	return(nRes);
}
double Multi(int n1, int n2)
{
	double nRes;
	nRes = n1 * n2;
	return(nRes);
}
double Div(int n1, int n2)
{
	double nRes;
	nRes = (double) n1 / n2;
	return(nRes);
}